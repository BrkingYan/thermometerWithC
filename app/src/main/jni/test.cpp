    double* normalize(short* arr,int length){
        int len  = length;
        short *arrCopy = (short*)malloc(sizeof(short)*len);
        memcpy(arrCopy,arr,sizeof(short)*len);

        for (int i = 0; i < len; ++i) {
            if (arrCopy[i] < 0){
                arrCopy[i] = - arrCopy[i];
            }
        }

        short max = 0;
        for (int j = 0; j < len; ++j) {
            if (arrCopy[j] > max){
                max = arrCopy[j];
            }
        }
        double *result =  (double*)malloc(sizeof(double)*len);
        for (int k = 0; k < len; ++k) {
            result[k] = (double)arr[k] / max;
        }
        return result;
    }
